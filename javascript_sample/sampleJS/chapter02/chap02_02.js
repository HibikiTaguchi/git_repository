// 変数num1に"1"を代入する。
let num1 = 1;
// コピーする。
let num2 = num1;
// コピー後の変数num2に"2"を上書きする。
num2 = 2;
document.write("num1=" + num1 + "<br />");

// オブジェクトを代入する。
let obj1 = new Object();
// プロパティーobj.numに"1"を代入する。
obj1.num = 1;
// コピーする。
let obj2 = obj1;
// プロパティーobj2.numに"2"を上書きする。
obj2.num = 2;
document.write("obj1.num=" + obj1.num + "<br />");