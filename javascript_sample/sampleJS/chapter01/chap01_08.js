// 加算と乗算
let a = 2, b = 3;
// 変数bの値×10＋変数aの値の演算結果を表示する。
document.write(a + b * 10);
document.write('<br />');
// （変数aの値＋変数bの値）×10の演算結果を表示する。
document.write((a + b) * 10);
document.write('<br />');

// 文字列連結と加算
a = 100, b = 120;
// 文字列“合計で“、変数aの値、変数bの値、文字列" 円です。"をつなげて表示する。
document.write("合計で " + a + b + " 円です。");
document.write("<br />");
// 文字列“合計で“、変数aの値＋変数bの値の演算結果、文字列" 円です。"をつなげて表示する。
document.write("合計で " + (a + b) + " 円です。");