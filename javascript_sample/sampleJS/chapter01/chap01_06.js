// 変数aに2、変数bに3を代入する。
const a = 2, b = 3;
//　変数answerに、[a ＋ b]の演算結果を代入する。
let answer = a + b;
document.write(answer + "<br />");
//　変数answerに、[a － b]の演算結果を代入する。
answer = a - b;
document.write(answer + "<br />");
//　変数answerに、[a × b]の演算結果を代入する。
answer = a * b;
document.write(answer + "<br />");
//　変数answerに、[a ÷ b]の演算結果を代入する。
answer = a / b;
document.write(answer + "<br />");